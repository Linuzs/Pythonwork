number1 = 1
number2 = 2
operator = input("Please input operator(+ - * /):")
if operator == "+":
    print(number1 + number2)
elif operator == "-":
    print(number1 - number2)
elif operator == "*":
    print(number1 * number2)
elif operator == "/":
    print(number1 / number2)
else:
    print("Re input!")
